/**
 * 模糊查询大类
 * @param {*} name 
 */
export const search = (name) => {
    return new Promise((resolve, reject) => {
        let result = []
        recursionSearch(data, name, result)
        if (result.length > 0) {
            resolve(result)
        } else {
            reject()
        }
    })
}

function recursionSearch(data, name, result) {
    data.forEach(item => {
        if (item.name.indexOf(name) >= 0) {
            result.push(item)
        }
        if (item.children && item.children.length > 0) {
            return recursionSearch(item.children, name, result)
        }
    })
}

/**
 * 点击节点展开
 * @param {*} id 
 */
export const expendNodes = (id) => {
    return new Promise((resolve, reject) => {
        let totalList = []
        //拆除来所有的children到第一层
        data.forEach(item => {
            getDeepChildrens(totalList, item, 0)
        })

        let list = []
        for (let item of totalList) {
            if (item.parentId.toString() === id) {
                const { children, ...reset } = item
                list.push({
                    ...reset
                })
            }
        }
        if (list.length > 0) {
            resolve(list)
        } else {
            reject()
        }
    })
}

/**
 * 递归数组,把所有children都拆出来到第一层
 * @param {*} list 
 * @param {*} item 
 */
function getDeepChildrens(list, item, parentId) {
    const { children, ...reset } = item
    list.push(
        {
            ...reset,
            parentId,
        }
    )
    if (children && children.length > 0) {
        children.forEach(child => {
            getDeepChildrens(list, child, item.id)
        })
    }
}
